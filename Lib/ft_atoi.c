#include "libft.h"

int	ft_atoi(const char *str)
{
	int a;
	int i;
	int m;

	m = 1;
	i = 0;
	a = 0;
	while (str[i] == '\t' || str[i] == '\v' || str[i] == '\f' || str[i] == '\r'
	|| str[i] == '\n' || str[i] == ' ')
		i++;
	if (str[i] == '-' || str[i] == '+')
	{
		if (str[i] == '-')
			m = -1;
		i++;
	}
	while (str[i] >= '0' && str[i] <= '9')
	{
		a *= 10;
		a += str[i] - 48;
		i++;
	}
	a *= m;
	return (a);
}
